// Generates src/brand-icons.tsx from a fetched Simple Icons dataset.
//
// Usage:
//   node scripts/gen-brand-icons.mjs            # reads .brand-icons.json (fetched set)
//
// The scripts/brand-icons.json dataset is produced by scripts/fetch-brand-icons.mjs
// (each brand's single <path> from the Simple Icons project, https://simple-icons.org,
// CC0-1.0). Path data is baked inline so the shipped icons are self-hosted (no
// runtime CDN — see CLAUDE.md §4.2). Re-run only when adding/removing a brand.
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(fileURLToPath(new URL(".", import.meta.url)), "..");
const icons = JSON.parse(fs.readFileSync(path.join(root, "scripts", "brand-icons.json"), "utf8"));

// Friendlier export base names (the "Icon" suffix is appended below). Anything not
// listed keeps its Simple Icons title-derived name.
const rename = {
  NextDotJs: "Nextjs",
  NodeDotJs: "Nodejs",
  ReactBrand: "Reactjs",
  TailwindCss: "Tailwind",
  AppleBrand: "AppleBrand",
};

const rows = icons
  .filter((o) => o.d && o.d.length > 10)
  .map((o) => {
    const base = rename[o.name] || o.name;
    return { export: base + "Icon", label: o.title, slug: o.slug, d: o.d };
  })
  .sort((a, b) => a.export.localeCompare(b.export));

const S = (v) => JSON.stringify(v); // safe JS string literal (handles any char)

const header = `// twico-ui/icons — brand icons.
//
// Lucide (https://lucide.dev) intentionally ships NO brand/logo icons, so this file
// adds a curated set of popular brand marks (social + developer tooling) as zero-
// dependency, inline-SVG React components — re-exported from "twico-ui/icons"
// alongside the full Lucide set.
//
//   import { GithubIcon, VercelIcon } from "twico-ui/icons";
//   <GithubIcon size={20} />
//
// API mirrors Lucide: \`size\` (number | string, default 24), \`color\` (default
// "currentColor", applied as the FILL since brand marks are solid, not stroked),
// plus any other SVG prop. Pass \`title\` to label the icon for assistive tech;
// otherwise it is \`aria-hidden\`. forwardRef-friendly, SSR-safe (no hooks).
//
// Path data: the Simple Icons project (https://simple-icons.org), CC0-1.0. Brand
// names/logos are trademarks of their respective owners; inclusion here is neither
// an endorsement by nor an affiliation with those owners.
//
// GENERATED by scripts/gen-brand-icons.mjs — do not edit by hand.

import * as React from "react";

export interface BrandIconProps extends Omit<React.SVGProps<SVGSVGElement>, "title"> {
  /** Width and height of the icon in pixels (or any CSS length). Defaults to 24. */
  size?: number | string;
  /** Fill color. Defaults to "currentColor" so the icon inherits the text color. */
  color?: string;
  /** Accessible label. When set the icon is exposed to AT; otherwise it is aria-hidden. */
  title?: string;
}

export type BrandIcon = React.ForwardRefExoticComponent<
  BrandIconProps & React.RefAttributes<SVGSVGElement>
>;

function brand(displayName: string, path: string): BrandIcon {
  const Comp = React.forwardRef<SVGSVGElement, BrandIconProps>(function BrandIcon(
    { size = 24, color = "currentColor", title, ...rest },
    ref,
  ) {
    return React.createElement(
      "svg",
      {
        ref,
        xmlns: "http://www.w3.org/2000/svg",
        width: size,
        height: size,
        viewBox: "0 0 24 24",
        fill: color,
        role: title ? "img" : undefined,
        "aria-hidden": title ? undefined : true,
        "aria-label": title || undefined,
        ...rest,
      },
      title ? React.createElement("title", null, title) : null,
      React.createElement("path", { d: path }),
    );
  });
  Comp.displayName = displayName;
  return Comp;
}
`;

const decls = rows
  .map((r) => `export const ${r.export} = /*#__PURE__*/ brand(${S(r.export)}, ${S(r.d)});`)
  .join("\n");

const listType = `

/** Metadata for every brand icon — handy for building galleries / icon pickers. */
export interface BrandIconMeta {
  /** The exported component name, e.g. "GithubIcon". */
  name: string;
  /** Human label, e.g. "GitHub". */
  label: string;
  /** Simple Icons slug, e.g. "github". */
  slug: string;
  /** The icon component itself. */
  Icon: BrandIcon;
}

export const brandIcons: BrandIconMeta[] = [
`;
const listBody = rows
  .map((r) => `  { name: ${S(r.export)}, label: ${S(r.label)}, slug: ${S(r.slug)}, Icon: ${r.export} },`)
  .join("\n");
const listEnd = `
];

/** Just the exported component names of every brand icon. */
export const brandIconNames: string[] = brandIcons.map((b) => b.name);
`;

fs.writeFileSync(
  path.join(root, "src", "brand-icons.tsx"),
  header + "\n" + decls + listType + listBody + listEnd,
);
console.log(`wrote src/brand-icons.tsx with ${rows.length} brand icons:`);
console.log(rows.map((r) => r.export).join(", "));
