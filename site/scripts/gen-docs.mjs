// Turns the `twico-docs-extract` workflow output into:
//   - src/demos/<Name>Demo.jsx  (one live demo per component)
//   - src/data/components.js     (summaries + props tables for the reference)
// Usage: node scripts/gen-docs.mjs <path-to-workflow-output.json>
import { readFile, writeFile, mkdir } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const root = resolve(here, ".."); // site/

const srcPath = process.argv[2];
if (!srcPath) {
  console.error("Pass the workflow output JSON path as the first argument.");
  process.exit(1);
}

const raw = JSON.parse(await readFile(srcPath, "utf8"));
const comps = (raw.result && raw.result.components) || raw.components || [];
if (!comps.length) {
  console.error("No components found in the workflow output.");
  process.exit(1);
}

await mkdir(resolve(root, "src/demos"), { recursive: true });

const slug = (n) => n.toLowerCase();
const data = [];
let unsafe = 0;

for (const c of comps) {
  const code = String(c.demoCode || "").replace(/\r\n/g, "\n").trimEnd() + "\n";
  await writeFile(resolve(root, `src/demos/${c.name}Demo.jsx`), code);
  if (c.buildSafe === false) unsafe++;
  data.push({
    name: c.name,
    slug: slug(c.name),
    group: c.group,
    summary: c.summary,
    importName: c.importName,
    propsRows: c.propsRows || [],
    snippet: c.snippet || "",
  });
}

data.sort((a, b) => a.name.localeCompare(b.name));

const out =
  "// AUTO-GENERATED by scripts/gen-docs.mjs from the twico-docs-extract workflow — do not edit by hand.\n" +
  "export const components = " +
  JSON.stringify(data, null, 2) +
  ";\n";
await writeFile(resolve(root, "src/data/components.js"), out);

console.log(`Wrote ${data.length} demos + components.js (${unsafe} demo(s) flagged not-build-safe).`);
