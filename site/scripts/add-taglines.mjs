const mod = await import("../src/data/components.js?ts=" + Date.now());
const components = mod.components.map((c) => ({
  ...c,
  tagline: TAGLINES[c.name] || c.tagline || (c.summary || "").split(". ")[0],
}));

const missing = components.filter((c) => !TAGLINES[c.name]).map((c) => c.name);
const out =
  "// AUTO-GENERATED by scripts/gen-docs.mjs from the twico-docs-extract workflow — do not edit by hand.\n" +
  "export const components = " +
  JSON.stringify(components, null, 2) +
  ";\n";
await writeFile(FILE, out);
console.log("Added taglines to " + components.length + " components.");
if (missing.length) console.log("No explicit tagline for:", missing.join(", "));
